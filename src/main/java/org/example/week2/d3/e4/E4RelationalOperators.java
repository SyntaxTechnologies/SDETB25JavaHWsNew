package org.example.week2.d3.e4;

public class E4RelationalOperators {
    public static void main(String[] args) {
// Create Variables
        int age = 18;
        int temperature = 30;
        int score = 75;

        // Use Relational Operators with Direct Comparisons

        // Check if age is at least 18
        System.out.println(age >= 18);      // Expected: true

        // Check if temperature is below 35
        System.out.println(temperature < 35); // Expected: true

        // Check if score is exactly 75
        System.out.println(score == 75);    // Expected: true

        // Check if score is not 100
        System.out.println(score != 100);   // Expected: true

        // Check if age is less than 20
        System.out.println(age < 20);       // Expected: tru
    }
}
