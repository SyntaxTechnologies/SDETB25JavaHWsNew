package org.example.week2.d3.e4;

public class E4RelationalOperators {
    public static void main(String[] args) {


// Step 1: Declare Variables with Specific Values
        int age = 18;
        int temperature = 30;
        int score = 75;

// Step 2: Write Relational Comparisons (Ensure the test cases pass)

        boolean isAgeGreaterOrEqual = age>=18;
        boolean isTemperatureGreater = temperature < 35;
        boolean isScoreEquals = score == 75;
        boolean isScoreNot = score != 100;
        boolean isAgeLessThan = age < 20;


// Step 3: Print Each Comparison Result
        System.out.println(isAgeGreaterOrEqual);
        System.out.println(isTemperatureGreater);
        System.out.println(isScoreEquals);
        System.out.println(isScoreNot);
        System.out.println(isAgeLessThan);



    }
}
